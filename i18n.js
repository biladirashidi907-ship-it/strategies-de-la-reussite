let currentTranslations = {};
let currentLang = 'fr';

function loadLanguage(lang) {
    currentLang = lang || localStorage.getItem('estra_language') || 'fr';
    if (translations[currentLang]) {
        currentTranslations = translations[currentLang];
    } else {
        currentTranslations = translations['fr'];
    }
    localStorage.setItem('estra_language', currentLang);
    return currentLang;
}

function t(key) {
    if (currentTranslations[key]) return currentTranslations[key];
    if (translations['fr'] && translations['fr'][key]) return translations['fr'][key];
    return key;
}

function applyTranslations(context = document) {
    currentLang = localStorage.getItem('estra_language') || 'fr';
    loadLanguage(currentLang);
    
    context.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const translation = t(key);
        if (translation && !el.hasAttribute('data-i18n-html')) {
            if (el.tagName === 'INPUT' && (el.type === 'text' || el.type === 'email' || el.type === 'search')) {
                // Skip input values
            } else {
                el.textContent = translation;
            }
        }
    });
    
    context.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        const translation = t(key);
        if (translation) el.placeholder = translation;
    });
    
    const titleEl = document.querySelector('title');
    if (titleEl && titleEl.hasAttribute('data-i18n')) {
        document.title = t(titleEl.getAttribute('data-i18n'));
    }
}

// 🆕 VÉRIFICATION DE SESSION
function checkSession() {
    const token = localStorage.getItem('estra_token');
    const loggedIn = localStorage.getItem('estra_logged_in');
    
    // Pages publiques
    const publicPages = [
        'login.html', 'session-expired.html', '404.html',
        'about.html', 'terms.html', 'privacy.html', 'help.html'
    ];
    const currentPage = window.location.pathname.split('/').pop() || 'index.html';
    
    if (publicPages.includes(currentPage)) {
        return true;
    }
    
    if (!token || loggedIn !== 'true') {
        window.location.href = 'session-expired.html';
        return false;
    }
    
    return true;
}

// 🆕 FONCTION DE DÉCONNEXION UNIVERSELLE
function logout() {
    localStorage.removeItem('estra_token');
    localStorage.removeItem('estra_refresh_token');
    localStorage.removeItem('estra_logged_in');
    window.location.href = 'login.html';
}

// Auto-initialize
document.addEventListener('DOMContentLoaded', () => {
    loadLanguage();
    applyTranslations();
    
    // Vérifier la session pour les pages protégées
    checkSession();
    
    window.addEventListener('storage', (e) => {
        if (e.key === 'estra_language' && e.newValue) {
            loadLanguage(e.newValue);
            applyTranslations();
        }
    });
});

// Expose globally
window.t = t;
window.applyTranslations = applyTranslations;
window.loadLanguage = loadLanguage;
window.currentLang = currentLang;
window.checkSession = checkSession;
window.logout = logout;